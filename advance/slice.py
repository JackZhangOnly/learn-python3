L = ['Mike', 'Bob', 'Jim', 'Kimmy', 'Lily']

print ('L[0:3] is', L[0:3])
print ('L[:3] is', L[:3])
print ('L[1:3] is', L[1:3])
print ('L[1:2] is', L[1:2])
print ('L[-2:] is', L[-2:])

R = list(range(100))
print ('R[:10] is', R[:10])
print('R[-10:] is', R[-10:])
print('R[10:20] is', R[10:20])
print('R[:10:2] is', R[:10:2])
print('R[::5] is', R[::5])